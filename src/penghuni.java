    private void datatable() {
        DefaultTableModel tbl=new DefaultTableModel();
        tbl.addColumn("NIK");
        tbl.addColumn("Nama");
        tbl.addColumn("Alamat");
        tbl.addColumn("No HP");
        tbl.addColumn("Tanggal Lahir");
        tbl.addColumn("Registrasi");
        tbl.addColumn("No Kamar");
        tblpenghuni.setModel(tbl);
        try{
            Statement statement=(Statement)Config.configDB().createStatement();
            ResultSet res=statement.executeQuery("SELECT * FROM penghuni");
            while(res.next())
            {
                tbl.addRow(new Object[]{
                res.getString("id_penghuni"),
                res.getString("nama"),
                res.getString("nik"),
                res.getString("alamat"),
                res.getString("no_handphone"),
                res.getString("tanggal_lahir"),
                res.getString("registrasi"),
                res.getString("no_kamar")
            }); 
            tblpenghuni.setModel(tbl);
            }
        }catch (Exception e) {
//                JOptionPane.showMessageDialog(rootPane, "salah");
    }
}
    
