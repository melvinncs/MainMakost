    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbll = (DefaultTableModel)tblpenghuni.getModel();
        try{
            String sql = "DELETE FROM penghuni WHERE NIK= '"+nik+"'";
            java.sql.Connection conn = (Connection)Config.configDB();
            Statement st = conn.createStatement();
            int confirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus data ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION) {
                int row = st.executeUpdate(sql);
                if (row==1) {
                    JOptionPane.showMessageDialog(this, "data berhasil dihapus");
                    conn.close();
                }
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        datatable();
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbll = (DefaultTableModel)tblpenghuni.getModel();
        jTabbedPane1.setSelectedIndex(1);
        try {
            Statement statement = (Statement)Config.configDB().createStatement();
            ResultSet res = statement.executeQuery("Select * from penghuni where NIK = '" + nik + "'");
            if (res.next()) {
                txtnikedit.setText(res.getString("NIK"));
                txtnamaedit.setText(res.getString("Nama"));
                txtalamatedit.setText(res.getString("Alamat"));
                txtnohpedit.setText(res.getString("No_Handphone"));
                txttanggallahiredit.setDate(res.getDate("Tanggal_lahir"));
                txtregistrasiedit.setText(res.getString("Registrasi"));
                cbnokamaredit.setSelectedItem(res.getString("No_kamar"));
            }else{
                JOptionPane.showMessageDialog(null, nik);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_btneditActionPerformed

