    private void tbl_kamarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_kamarMouseClicked
        // TODO add your handling code here:
        int baris = tbl_kamar.rowAtPoint(evt.getPoint());
        String no_kamar = tbl_kamar.getValueAt(baris, 0).toString();
        txt_nokamar.setText(no_kamar);
        txt_nokamar.disable();
        if(tbl_kamar.getValueAt(baris, 1)==null){
            txtharga.setText("");
        }else{
            txtharga.setText(tbl_kamar.getValueAt(baris, 1).toString());
        }
        if(tbl_kamar.getValueAt(baris, 2)==null){
            cbkapasitas.setSelectedItem(this);
        }else{
            cbkapasitas.setSelectedItem(tbl_kamar.getValueAt(baris, 2).toString());
        }
        if(tbl_kamar.getValueAt(baris, 3)==null){
            txtlantai.setText("");
        }else{
            txtlantai.setText(tbl_kamar.getValueAt(baris, 3).toString());
        }
        if(tbl_kamar.getValueAt(baris, 4)==null){
            txtfasilitas.setText("");
        }else{
            txtfasilitas.setText(tbl_kamar.getValueAt(baris, 4).toString());
        }
        if(tbl_kamar.getValueAt(baris, 5)==null){
            cmbstatus.setSelectedItem(this);
        }else{
            cmbstatus.setSelectedItem(tbl_kamar.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_tbl_kamarMouseClicked

