    private void btndetailkamarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndetailkamarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbl = (DefaultTableModel)tbl_kamar.getModel();
        String no_kamar = tbl.getValueAt(tbl_kamar.getSelectedRow(), 0).toString();
        jTabbedPane1.setSelectedIndex(1);
        try{
            Statement st = (Statement)Config.configDB().createStatement();
            ResultSet rs = st.executeQuery("Select * from kamar where no_kamar = '" + no_kamar + "'");
            if(rs.next()){
                txtnokamardetail.setText(rs.getString(1));
                txthargadetail.setText(rs.getString(2));
                txtkapasitasdetail.setText(rs.getString(3));
                txtlantaidetail.setText(rs.getString(4));
                txtfasilitasdetail.setText(rs.getString(5));
                txtstatusdetail.setText(rs.getString(6));
            }else{
                JOptionPane.showMessageDialog(null, no_kamar);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_btndetailkamarActionPerformed

